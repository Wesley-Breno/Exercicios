codes = {
    'a': ".-",
    'b': "-...",
    'c': "-.-.",
    'd': "-..",
    'e': ".",
    'f': "..-.",
    'g': "--.",
    'h': "....",
    'i': "..",
    'j': ".---",
    'k': "-.-",
    'l': ".-..",
    'm': "--",
    'n': "-.",
    'o': "---",
    'p': ".--.",
    'q': "--.-",
    'r': ".-.",
    's': "...",
    't': "-",
    'u': "..-",
    'v': "...-",
    'w': ".--",
    'x': "-..-",
    'y': "-.--",
    'z': "--.."
}


class Solution:
    def uniqueMorseRepresentations(self, words: List[str]) -> int:
        transformations = set()
        for word in words:
            transformation = ''
            for letter in word:
                transformation += codes[letter]
            transformations.add(transformation)

        return len(transformations)
